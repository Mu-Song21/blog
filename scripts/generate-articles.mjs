import { writeFileSync, mkdirSync } from 'fs'
import { resolve, dirname } from 'path'
import { fileURLToPath } from 'url'
import { loadArticlesFromMarkdown } from './loadArticles.mjs'

const __dirname = dirname(fileURLToPath(import.meta.url))
const outFile = resolve(__dirname, '../src/data/articles.generated.js')

const articles = loadArticlesFromMarkdown()
mkdirSync(dirname(outFile), { recursive: true })

const body = `/** Auto-generated by scripts/generate-articles.mjs — do not edit */
export const DEFAULT_ARTICLES = ${JSON.stringify(articles, null, 2)}
`

writeFileSync(outFile, body, 'utf8')
console.log(`✅ Generated ${articles.length} articles → src/data/articles.generated.js`)
